//! A module for geographic enums

// Enables use as an iterable and computation of length
use strum_macros::{EnumIter, EnumCount};

/// Directions
#[derive(Debug, EnumIter, EnumCount, Copy, Clone)]
pub enum Direction {
    North, South, East, West
}

